class EllysTimeMachine {
public:
	string getTime(string);
};

string EllysTimeMachine::getTime(string time) {
	int h, m, hh, mm;
	h = 10*(time[0] - '0') + (time[1] - '0');
	m = 10*(time[3] - '0') + (time[4] - '0');
	int h2m[13] = {0, 5, 10, 15, 20, 25, 30, 35, 40, 45, 50, 55, 0};

	hh = m/5;
	if (hh == 0) hh = 12;
	mm = h2m[h];
	char res[6];
	sprintf(res, "%02d:%02d", hh, mm);
	return res;
}




